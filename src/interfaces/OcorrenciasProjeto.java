    private void deletarOcorrenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarOcorrenciaActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja remover essa ocorrência do histórico ?", "Remover ocorrência", JOptionPane.YES_NO_OPTION);
              
            if(resposta == 0) 
            {                   
                projeto.deletarOcorrencia(linhaOcorrencia);
                projeto.getOcorrencias(Main.linhaProjeto);
                deletarOcorrencia.setEnabled(false);
                mostrainfo();
            }
    }//GEN-LAST:event_deletarOcorrenciaActionPerformed

