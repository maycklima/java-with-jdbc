    public void enviarPDF()
    {           
       File diretorio = new File(Usuario.getSalvar_em() + "\\" + idProjeto);
       
        
       if (!diretorio.exists())
            diretorio.mkdirs();
 
        
       try {
            // Move o arquivo para o novo diretorio
            
            
            Files.deleteIfExists(Paths.get(diretorio + "\\" + "Cancelamento do projeto.pdf"));
            Files.copy(Paths.get(caminho), Paths.get(diretorio + "\\" + "Cancelamento do projeto.pdf"));
        } catch (FileAlreadyExistsException ex) {
            JOptionPane.showMessageDialog(null, "Já existe um arquivo com esse nome");
        } catch (IOException ex) {
            Logger.getLogger(CancelarProjeto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
        
