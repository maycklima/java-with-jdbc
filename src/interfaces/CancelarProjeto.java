    private void jbCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCancelarActionPerformed
           
        if(txtDataCancelamento.getText().equals("  /  /    "))
        {
            JOptionPane.showMessageDialog(null, "Digite a data do cancelamento");
        }else if(txtFile.getText().isEmpty())
        {
            int resposta  = JOptionPane.showConfirmDialog(null , "Você não selecionou nenhum arquivo, deseja continuar mesmo assim?", "Adicionar um arquivo", JOptionPane.YES_NO_OPTION);

            switch (resposta) 
            {
                case 0:
                    
                    pro.cancelarProjeto(idProjeto,txtDataCancelamento.getText(),txtOrientador.getText());
                    Main.limparProjeto();
                    if(jRbOrientador.isSelected())
            {
            if(jrbSuperior.isSelected())
            {
                orientador.getOrientadorSuperior();
            }else if(jrbMedio.isSelected())
            {
                orientador.getOrientadorMedio();
            }else{
                orientador.getOrientador("");
            }
        }
        else if(JRbALuno.isSelected())
        {
          if(jrbSuperior.isSelected())
            {
                aluno.getAlunoSuperior();
            }else if(jrbMedio.isSelected())
            {
                aluno.getAlunoMedio();
            }else{
                aluno.getAluno("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Escolha entre aluno e orientador pra fazer a busca!");
        }
                    dispose();
                    break;
               
        }
        }else
        {
            pro.cancelarProjeto(idProjeto, txtDataCancelamento.getText(),txtOrientador.getText());
            Main.limparProjeto();
            enviarPDF();
            if(jRbOrientador.isSelected())
            {
            if(jrbSuperior.isSelected())
            {
                orientador.getOrientadorSuperior();
            }else if(jrbMedio.isSelected())
            {
                orientador.getOrientadorMedio();
            }else{
                orientador.getOrientador("");
            }
        }
        else if(JRbALuno.isSelected())
        {
          if(jrbSuperior.isSelected())
            {
                aluno.getAlunoSuperior();
            }else if(jrbMedio.isSelected())
            {
                aluno.getAlunoMedio();
            }else{
                aluno.getAluno("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Escolha entre aluno e orientador pra fazer a busca!");
        }
            dispose();
        }
    }//GEN-LAST:event_jbCancelarActionPerformed

