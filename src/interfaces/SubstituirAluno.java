    public static void populaComboBoxAlunoNovo()
    {
        idAlunos.clear();
        String sql = "SELECT id_aluno,nome_aluno,curso_aluno FROM aluno WHERE aluno_excluido = 0 order by nome_aluno ";
        try 
        {
            db.DAO.instrucao = db.DAO.conexao.prepareStatement(sql);
            db.DAO.resultado = db.DAO.instrucao.executeQuery();
            
            while (db.DAO.resultado.next())
            {
                idAlunos.add(Integer.parseInt(resultado.getString("id_aluno"))); 
                jComboBoxAlunoNovo.addItem(resultado.getString("nome_aluno"));                
            }
            
        } catch (SQLException e) 
        {
            if(e instanceof com.mysql.jdbc.exceptions.jdbc4.CommunicationsException)
            {              
                JOptionPane.showMessageDialog(null, "Sua sessão expirou, faça o login novamente!");
                System.exit(0);
            }else
            {
                JOptionPane.showMessageDialog(null, e);                
            }   
         }
               
    }
    
    public void substituirAluno()
    {    
         
         int idAlunoAntigo;
         int aluno1;
         int aluno2;
         
       
        try 
        {
                        
            idAlunoAntigo = Integer.parseInt(String.valueOf(jTableMain.getValueAt(linha_selecionada, 17)));
            
            
            if(String.valueOf(jTableMain.getValueAt(linha_selecionada, 15)).equals("1"))
            {
                p.setAluno1_substituto(true);
            }else
            {
                p.setAluno1_substituto(false);
            }
            
            if(String.valueOf(jTableMain.getValueAt(linha_selecionada, 16)).equals("1"))
            {
                p.setAluno2_substituto(true);
            }else
            {
                p.setAluno2_substituto(false);
            }
            
                       
            aluno1 = Integer.parseInt(String.valueOf(jTableMain.getValueAt(linha_selecionada, 18)));
            
            if(String.valueOf(jTableMain.getValueAt(linha_selecionada, 19)).equals("null"))
            {
                                
                    idAlunoNovo = idAlunos.get(jComboBoxAlunoNovo.getSelectedIndex());
                    
                    nomeAluno = String.valueOf(jComboBoxAlunoNovo.getSelectedItem());
                    p.setAlunoAntigo(aluno1);
                    p.setAlunoNovo(idAlunoNovo);

                    p.setAluno1(idAlunoNovo);
                    p.setAluno2(0);
                    p.setAluno1_substituto(true);
                    p.setAluno2_substituto(p.isAluno2_substituto());                    
                    p.setData_substituicao1(txtDataSubstituicao.getText());
                    p.setData_substituicao2(String.valueOf(jTableMain.getValueAt(linha_selecionada, 22)));
            }
            else 
            {
            aluno2 = Integer.parseInt(String.valueOf(jTableMain.getValueAt(linha_selecionada, 19)));
            
            
            if( idAlunoAntigo == aluno1)
            {                
                idAlunoNovo = idAlunos.get(jComboBoxAlunoNovo.getSelectedIndex());
                nomeAluno = String.valueOf(jComboBoxAlunoNovo.getSelectedItem());
                p.setAlunoAntigo(aluno1);
                p.setAlunoNovo(idAlunoNovo);
                
                p.setAluno1(idAlunoNovo);
                p.setAluno2(aluno2);
                p.setAluno1_substituto(true);
                p.setAluno2_substituto(p.isAluno2_substituto());
                p.setData_substituicao1(txtDataSubstituicao.getText());
                p.setData_substituicao2(String.valueOf(jTableMain.getValueAt(linha_selecionada, 22)));
                
                
                
            }
            else if(idAlunoAntigo == aluno2)
            {
                idAlunoNovo = idAlunos.get(jComboBoxAlunoNovo.getSelectedIndex());
                nomeAluno = String.valueOf(jComboBoxAlunoNovo.getSelectedItem());
                p.setAlunoAntigo(aluno2);
                p.setAlunoNovo(idAlunoNovo); 
                
                p.setAluno1(aluno1);
                p.setAluno2(idAlunoNovo);
                p.setAluno1_substituto(p.isAluno1_substituto());
                p.setAluno2_substituto(true);
                p.setData_substituicao1(String.valueOf(jTableMain.getValueAt(linha_selecionada, 21)));
                p.setData_substituicao2(txtDataSubstituicao.getText());
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Caiu aqui!");
            }
            }

                              
        }        
        catch (HeadlessException | NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        p.setNomeProjeto(nomeProjeto);
        p.setVigenciaProjeto(txtVigencia.getText());
        
        pro.substituirAluno(p, Main.linhaProjeto, txtAlunoAntigo.getText(),String.valueOf(jComboBoxAlunoNovo.getSelectedItem()), nomeOrientador, txtDataSubstituicao.getText());
        aluno.getAluno("");
        dispose();   
    }
    
    public void enviarPDF()
    {             
             
       File diretorio = new File(Usuario.getSalvar_em() + "\\" + idProjeto);
       
        
       if (!diretorio.exists())
            diretorio.mkdirs();
 
        
       try {
            // Move o arquivo para o novo diretorio
            
            
            //JOptionPane.showMessageDialog(null, diretorio);
            Files.deleteIfExists(Paths.get(diretorio + "\\" + "Substituição de " + txtAlunoAntigo.getText()+ " por " + nomeAluno + ".pdf"));
            Files.copy(Paths.get(caminho), Paths.get(diretorio + "\\" + "Substituição de " + txtAlunoAntigo.getText()+ " por " + nomeAluno + ".pdf"));
        } catch (FileAlreadyExistsException ex) {
            JOptionPane.showMessageDialog(null, "Já existe um arquivo com esse nome");
        } catch (IOException ex) {
            Logger.getLogger(SubstituirAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
        
